// A template for implementing the Two Pointers algorithm pattern with start and end pointers.

public static int startAndEndPointers(int[] arr) {
    int left = 0;
    int right = arr.length - 1;

    while (left < right) {
        if (foo) {
            left++;
        } else if (bar) {
            right--;
        } else if (successCondition) {
            return 1;
        }
    }

    return -1;
}
